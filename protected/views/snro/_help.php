<h1 class="h1help">Pendahuluan</h1>
<p class="phelp">Modul Specific Number Range Object (SNRO) digunakan untuk menentukan <br/>
no dokumen otomatis. No dokumen dapat disetting berulang per bulan dan <br/>
per tahun, atau salah satu-nya.</p>
<h1 class="h1help">Relasi</h1>
<p class="phelp">SNRO ini digunakan di berbagai modul yang membutuhkan running no, <br/>
antara lain PR, PO, Surat Jalan, dan lain-lain. <br/>
Penentuan SNRO dilakukan di awal implementasi dan sudah memperhitungkan <br/>
running no untuk jangka waktu 5 tahun.</p>
